class Position:
    def __init__(self, positionX,positionY):
        self.X = positionX
        self.Y=positionY
    def getX(self):
     return self.X

    def getY(self):
        return self.Y

    def setX(self, positionX):
        self.X = positionX

    def setY(self,positionY):
        self.Y = positionY